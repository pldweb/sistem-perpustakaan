    public function getJadwalShalat(Request $request) {
        // Ambil locationId dari query atau cookie
        $locationId = $request->query('locationId', $request->cookie('selectedLocation'));

        // Ambil semua lokasi
        $locations = $this->getLocations();

        // Simpan locationId ke dalam cookie jika ada
        if ($locationId) {
            Cookie::queue('selectedLocation', $locationId, 60 * 24); // Simpan 1 hari
        }

        $selectedLocationName = $locations[$locationId] ?? 'Pilih Lokasi';

        // Fetch jadwal berdasarkan locationId
        if ($locationId) {
            $response = Http::get("https://jadwalsholat.org/adzan/ajax/next/ajax.daily.php?id={$locationId}");
            $data = $response->json();

            $jadwal = [
                'shubuh' => $data['schedules']['shubuh'] ?? 'N/A',
                'dzuhur' => $data['schedules']['dzuhur'] ?? 'N/A',
                'ashar'  => $data['schedules']['ashr'] ?? 'N/A',
                'maghrib'=> $data['schedules']['maghrib'] ?? 'N/A',
                'isya'   => $data['schedules']['isya'] ?? 'N/A',
            ];
        } else {
            $jadwal = [
                'shubuh' => 'N/A',
                'dzuhur' => 'N/A',
                'ashar'  => 'N/A',
                'maghrib'=> 'N/A',
                'isya'   => 'N/A',
            ];
        }

        $currentTime = Carbon::now();

        $nextPrayer = null;
        $timeToNextPrayer = null;

        foreach ($jadwal as $prayer => $time) {
            if ($time !== 'N/A') {  // Pastikan waktu valid
                $prayerTime = Carbon::parse($time);

                // Jika waktu sholat lebih besar dari waktu saat ini
                if ($prayerTime->isFuture()) {
                    $nextPrayer = $prayer;
                    $timeToNextPrayer = $currentTime->diffInSeconds($prayerTime);
                    break;
                }
            }
        }

        if (!$nextPrayer) {
            $nextPrayer = 'Tidak ada sholat berikutnya hari ini.';
            $timeToNextPrayerFormatted = '00:00:00';
        } else {
            $timeToNextPrayerFormatted = Carbon::createFromTimestamp($timeToNextPrayer)->format('H:i:s');
        }

        $params = [
            'jadwal' => $jadwal,
            'locations' => $locations,
            'selectedLocationName' => $selectedLocationName,
            'currentTime' => $currentTime,
            'nextPrayer' => $nextPrayer,
            'timeToNextPrayerFormatted' => $timeToNextPrayerFormatted,
        ];

        return view('pages.jadwal-shalat.index-shalat', $params);
    }

