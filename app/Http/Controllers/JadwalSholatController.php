    public function getLocation() {

        $url = 'https://jadwalsholat.org/adzan/ajax.row.php'; // Ganti dengan URL yang relevan

        // Ambil konten HTML dari URL
        $response = Http::get($url);
        $html = $response->body();

        // Buat instance Crawler
        $crawler = new Crawler($html);

        // Ambil data lokasi
        $options = $crawler->filter('select#town-select option')->each(function ($node) {
            $id = $node->attr('value');
            $name = trim($node->text());
            return [
                'id' => $id,
                'name' => $name,
            ];
        });

        // Convert array ke associative array
        $locationsArray = [];
        foreach ($options as $option) {
            $locationsArray[$option['id']] = $option['name'];
        }

        // Kirim data ke view
        return view('pages.jadwal-shalat.index-lokasi', ['locations' => $locationsArray]);
    }

